$created = 0;
foreach ($names as $name) {
    $name = trim(mb_strtoupper($name));
    if ($name === '') {
        continue;
    }
    if (Category::find()->where(['name' => $name])->exists()) {
        continue;
    }

    $category = new Category();
    $category->name = $name;
    $category->description = null;

    if ($category->save()) {
        $created++;
        echo "CREATED: {$category->name}" . PHP_EOL;
    } else {
        echo "FAILED: {$name} => " . json_encode($category->getErrors()) . PHP_EOL;
    }
}

echo "Total nuevas categorías: {$created}" . PHP_EOL;

